export function useI18n() {
  const [language, setLanguage] = useState<Language>("en-US");

  useEffect(() => {
    invoke<string>("current_language")
      .then((value) => setLanguage(toLanguage(value)))
      .catch(() => setLanguage("en-US"));

    const unlisten = listen<LanguageChanged>(LANGUAGE_CHANGED_EVENT, (event) => {
      setLanguage(toLanguage(event.payload.language));
    });

    return () => {
      unlisten.then((cleanup) => cleanup());
    };
  }, []);

  return useMemo(() => {
    const dictionary = dictionaries[language];
    return {
      language,
      t: (key: I18nKey, values?: Record<string, string | number>) =>
        format(dictionary[key], values),
    };
  }, [language]);
}

export function toLanguage(language: string): Language {
  return language in dictionaries ? (language as Language) : "en-US";
}

function format(template: string, values?: Record<string, string | number>) {
  if (!values) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (_, key: string) =>
    Object.prototype.hasOwnProperty.call(values, key) ? String(values[key]) : "",
  );
}
